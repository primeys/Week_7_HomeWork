public class _08_RightAngleTriangle {

  public static void main(String[] args)
    {
        RightAngleTriangle();
    }


    public static void RightAngleTriangle(){
        for(int i = 0 ; i <5 ; i++){
            for(int j = 0 ; j<=i ; j++){
                System.out.print("@");
            }
            System.out.println("");
        }
    }

}
